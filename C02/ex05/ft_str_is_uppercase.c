int	ft_str_is_uppercase(char *str)
{
	int	i;

	i = 0;
	while (i[str])
	{
		if (!(i[str] >= 'A' && i[str] <= 'Z'))
		{
			return (0);
		}
		i++;
	}
	return (1);
}
